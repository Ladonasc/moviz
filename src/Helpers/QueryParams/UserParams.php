<?php

namespace App\Helpers\QueryParams;

class UserParams
{

    /**
     * @var string
     */
    private $email;

    /**
     * @return string
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string $email
     *
     * @return self
     */
    public function setEmail($email)
    {
        $this->email = trim($email);

        return $this;
    }
}
